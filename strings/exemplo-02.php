<?php

$nome = "Miqueias Rihs";

// Tudo para maiusculo
echo 'strtoupper // ' . strtoupper($nome);
echo "<br>";

// Tudo para minusculo
echo 'strtolower // ' . strtolower($nome);
echo "<br>";

// Deixa apenas a primeira letra de cada palavra maiuscula
echo 'ucwords // ' . ucwords($nome);
echo "<br>";

// Deixa apenas a primeira letra do texto maiuscula
echo 'ucfirst // ' . ucfirst($nome);
echo "<br>";

?>